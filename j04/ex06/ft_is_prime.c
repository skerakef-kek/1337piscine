int		ft_is_prime(int nb)
{
	int d;
	int r;

	d = 2;
	r = 1;
	if (nb <= 1)
		return (0);
	if (nb >= 2)
	{
		while (nb > d)
		{
			if (nb % d == 0)
				r = 0;
			d++;
		}
	}
	else
		r = 0;
	return (r);
}
